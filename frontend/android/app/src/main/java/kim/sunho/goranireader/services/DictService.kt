    lateinit var db: SQLiteDatabase
    fun init(context: Context) {
        val opener = AssetDatabase(context, "dict.db")
        db = opener.openDatabase()
    }

    fun search(word: String): List<DictWord> {
        val cands = baseCanidates(word)
        return cands.flatMap { cand ->
            getWord(cand)?.let { listOf(it) } ?: listOf()
        }
    }

    private fun getWord(word: String): DictWord? {
        var cursor: Cursor? = null
        try {
            cursor = db.rawQuery("select * from words where word = ?", arrayOf(word))
            while (cursor.moveToNext()) {
                val pron = cursor.getString(cursor.getColumnIndex("pron"))
                val defs = getDefs(word)
                return DictWord(word, pron, defs)
            }
            return null

        } catch (e: SQLiteException) {
            cursor?.close()
            return null
        }
    }

    private fun getDefs(word: String): List<DictDefinition> {
        var cursor: Cursor? = null
        try {
            cursor = db.rawQuery("select * from defs where word = ?", arrayOf(word))
            val out = ArrayList<DictDefinition>()
            while (cursor.moveToNext()) {
                val id = cursor.getInt(cursor.getColumnIndex("id"))
                val pos = cursor.getStringOrNull(cursor.getColumnIndex("pos"))
                val def = cursor.getString(cursor.getColumnIndex("def"))
                out.add(DictDefinition(id, pos, def))
            }
            return out

        } catch (e: SQLiteException) {
            cursor?.close()
        }
        return ArrayList()
    }

    private fun baseCanidates(word: String): List<String> {
        val word = word.toLowerCase()
        val out = ArrayList<String>(listOf(word))
        irregularPasts[word]?.let {
            out.add(it)
        }
        irregularCompletes[word]?.let {
            out.add(it)
        }
        val suffixes = listOf("s", "es", "y", "ed", "ing", "ion", "tion", "ation", "ition", "able", "ible", "al", "ial", "en", "en", "er", "est", "ful", "ic",  "ity", "ty", "ive", "ative", "itive", "less", "ly", "ment", "ness", "ous", "eous", "ious")
        for (suffix in suffixes) {
            word.trimmingSuffix("y$suffix")?.let {
                out.add(it + "ie")
            }

            word.trimmingSuffix("i$suffix")?.let {
                out.add(it + "y")
            }

            word.trimmingSuffix("al$suffix")?.let {
                out.add(it)
            }

            word.trimmingSuffix(suffix)?.let {
                out.add(it + "e")
                if (it.length >= 2 &&
                        it.charAtBack(1) == it.charAtBack(2)) {
                    // get -> getting
                    out.add(it.dropLast(1))
                }
                // go -> going
                out.add(it)
                out.add(it + "le")
                out.add(it + "y")
            }
        }
        return out.distinct()
    }

